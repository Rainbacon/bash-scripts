#!/bin/bash

IFS=',' read -r -a projects <<< "$GIT_PROJECTS"
format="|%-31s|%-80s|\n"

echo "------------------------------------------------------------------------------------------------------------------"
printf "$format" "Project" "Branch"
echo "------------------------------------------------------------------------------------------------------------------"
for project in "${projects[@]}"
do
  branch=$(git -C ~/Code/$project rev-parse --abbrev-ref HEAD 2>&1)
  printf "$format" "$project" "$branch"
done
echo "------------------------------------------------------------------------------------------------------------------"
